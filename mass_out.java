package starts;

public class mass_out {
	//output mass integer
		public static void out(int length, int[] arr ) {
			for(int i=0; i<length; i++) {
				System.out.print(arr[i]+" ");
			}
			System.out.println("\n");
			
		}
		
	//output mass string
		public static void out(int length, String[] arr ) {
			for(int i=0; i<length; i++) {
				System.out.print(arr[i]+" ");
			}
			System.out.println("\n");
			
		}
}
